    private void jAddInvenSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAddInvenSubmitButtonActionPerformed
        // TODO add your handling code here:
        try{
            
            String bottleNo = jAddInvenBottleNoFormattedTextField.getText();
            String cas = jAddInvenCASNoTextField.getText();
            String transactionId = jAddInvenTransactionIDTextField.getText();
            String pDate = jAddInvenPurchaseDateComboBox.getSelectedItem().toString();
            String pMon = Func.monthCast(jAddInvenPurchaseMonthComboBox.getSelectedItem().toString());
            String pYear;
            if(jAddInvenPurchaseYearFormattedTextField.getText().length()>3){
                pYear = jAddInvenPurchaseYearFormattedTextField.getText().substring(0,4);
            }
            else pYear = "null";
            String eDate = jAddInvenExpiryDateComboBox.getSelectedItem().toString();
            String eMon = Func.monthCast(jAddInvenExpiryMonthComboBox.getSelectedItem().toString());
            String eYear;
            if(jAddInvenExpiryYearFormattedTextField.getText().length()>3){
                eYear = jAddInvenExpiryYearFormattedTextField.getText().substring(0,4);
            }
            else eYear = "null";
            String purchaseDate = pYear+"-"+pMon+"-"+pDate;
            String expiryDate = eYear+"-"+eMon+"-"+eDate;
            String purchaseQty = jAddInvenPurchaseQtyFormattedTextField.getText();
            String remainingQty = jAddInvenRemainingQtyFormattedTextField.getText();
            String company = jAddInvenCompanyTextField.getText();
            String cost = jAddInvenCostFormattedTextField.getText();
            boolean flag = jAddInvenBorrowYesRadioButton.isSelected();
            String borrow;
            if(flag){
                borrow = "1";
            }
            else
                borrow = "0";
            boolean casflag = Chemical.checkCAS(cas);
            boolean botflag = Inventory.checkBottleNo(bottleNo);
            if(casflag&&(!botflag)){
                boolean f = Inventory.addInventory(bottleNo, cas, transactionId, purchaseDate, expiryDate, purchaseQty, remainingQty, company, cost, borrow);
                jAddSuccessDialog.setVisible(f);
                jAddFailedDialog.setVisible(!f);
            }
            else if(!casflag){
                jCASDoesntExistDialog.setVisible(true);
            }
            else{
                jBottleNoExistsDialog.setVisible(true);
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jAddInvenSubmitButtonActionPerformed

