            String pMon = Func.monthCast(jEditInvenPurchaseMonthComboBox.getSelectedItem().toString());
            String pYear;
            if(jEditInvenPurchaseYearFormattedTextField.getText().length()>3){
                pYear = jEditInvenPurchaseYearFormattedTextField.getText().substring(0,4);
            }
            else pYear = "null";
            String eDate = jEditInvenExpiryDateComboBox.getSelectedItem().toString();
            String eMon = Func.monthCast(jEditInvenExpiryMonthComboBox.getSelectedItem().toString());
            String eYear;
            if(jEditInvenExpiryYearFormattedTextField.getText().length()>3){
                eYear = jEditInvenExpiryYearFormattedTextField.getText().substring(0,4);
            }
            else eYear = "null";
