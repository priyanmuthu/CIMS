    void logIn(){
        String uname = jUserIdLoginTextField.getText();
        String password = new String(jPasswordLoginPasswordField.getPassword());
        if(User.checkUserID(uname)){
            if(User.verifyPassword(uname, password)){
                user = User.logIn(uname, password);
                Home.main(null);
                this.dispose();
            }
            else{
                System.out.println("Password incorrect");
            }
        }
        else{
            System.out.println("User Id doesnt exist");
        }
    }
    
